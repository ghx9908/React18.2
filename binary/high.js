/**
 * 获取优先级最低的lane
 * 其实就是最左则的1
 * 31个lane+0=32
 * 整数是4个字节表示，一个字节是8位，32位
 * @param {*} lanes 
 */
function getLowestPriorityLane(lanes) {
  // 12
  //31-12=11110100001001000000长度=19
  const index = 31 - Math.clz32(lanes);
  return 1 << index;
}
